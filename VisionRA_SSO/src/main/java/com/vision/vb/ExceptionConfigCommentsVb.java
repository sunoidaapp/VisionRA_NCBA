package com.vision.vb;

public class ExceptionConfigCommentsVb extends CommonVb{

	private String country = "";
	private String leBook = "";
	private String exceptionReference = "";
	private String comments = "";
	private int statusNt = 1;
	private int status = 0;

	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLeBook() {
		return leBook;
	}
	public void setLeBook(String leBook) {
		this.leBook = leBook;
	}
	public String getExceptionReference() {
		return exceptionReference;
	}
	public void setExceptionReference(String exceptionReference) {
		this.exceptionReference = exceptionReference;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public int getStatusNt() {
		return statusNt;
	}
	public void setStatusNt(int statusNt) {
		this.statusNt = statusNt;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}